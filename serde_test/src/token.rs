#[derive(Clone, PartialEq, Debug)]
pub enum Token<'a> {
    Bool(bool),
    Isize(isize),
    I8(i8),
    I16(i16),
    I32(i32),
    I64(i64),
    Usize(usize),
    U8(u8),
    U16(u16),
    U32(u32),
    U64(u64),
    F32(f32),
    F64(f64),
    Char(char),
    Str(&'a str),
    String(String),
    Bytes(&'a [u8]),
    ByteBuf(Vec<u8>),

    Option(bool),

    Unit,
    UnitStruct(&'a str),

    StructNewType(&'a str),

    EnumStart(&'a str, &'a [&'a str]),
    EnumUnit(usize),
    EnumNewType(usize),

    SeqStart(Option<usize>),
    SeqArrayStart(usize),
    SeqSep,
    SeqEnd,

    TupleStart(usize),
    TupleSep,
    TupleEnd,

    TupleStructStart(&'a str, usize),
    TupleStructSep,
    TupleStructEnd,

    MapStart(Option<usize>),
    MapSep,
    MapEnd,

    StructStart(&'a str, usize),
    StructSep,
    StructEnd,

    EnumSeqStart(usize, usize),
    EnumSeqSep,
    EnumSeqEnd,

    EnumMapStart(usize, usize),
    EnumMapSep,
    EnumMapEnd,
}
